function somaDoisNumeros(numero1, numero2) {  // para criar função, abrir parenteses e chaves. 
    const soma = parseInt (numero1) + parseInt (numero2);   //parseInt para garantir que serão numeros inteiros. 
    return soma;    // retorna o resultado da variavel "soma". 
}


console.log(somaDoisNumeros(2,2)*10)   //Mostrando o resultado *10   // sempre precisa chamar a função. 